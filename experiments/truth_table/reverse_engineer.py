"""
Algorithm RevEng — truth table reverse engineering (§3 of Leandro ALT 2009).

Replicates Table 2 from the paper: given a (n+1)-valued truth sub-table
generated by a known Ł formula, find an equivalent ŁNN and extract the formula.

Usage:
    python -m experiments.truth_table.reverse_engineer
"""

import time
import torch
import torch.nn as nn

from luknn.logic.connectives import evaluate_formula, truth_subtable
from luknn.network.luknn import make_network
from luknn.network.crystallization import (
    progressive_crystallize, crisp_crystallize_weights, crisp_crystallize_bias,
    representation_error,
)
from luknn.training.lm import lm_train
from luknn.training.obs_pruning import obs_prune
from luknn.extraction.extractor import extract_formula

# ---------------------------------------------------------------------------
# Target formulas from Table 2 of the paper (6 variables)
# ---------------------------------------------------------------------------

def formula_1(x1, x2, x3, x4, x5, x6):
    """i1 ⊗ i3 ⟹ i6  (simplest — should converge fast)"""
    from luknn.logic.connectives import tnorm, residuum
    return tnorm(x1, residuum(x3, x6))

def formula_2(x1, x2, x3, x4, x5, x6):
    """(i4 ⟹ i6) ⊗ (i6 ⟹ i2)"""
    from luknn.logic.connectives import tnorm, residuum
    return tnorm(residuum(x4, x6), residuum(x6, x2))

def formula_6(x1, x2, x3, x4, x5, x6):
    """Most complex formula from Table 2 (requires ≥4 hidden layers)."""
    from luknn.logic.connectives import tnorm, residuum
    return tnorm(
        tnorm(
            tnorm(residuum(tnorm(x4, x5), x6), residuum(tnorm(x1, x5), x2)),
            residuum(tnorm(x1, x2), x3),
        ),
        residuum(x6, x4),
    )

FORMULAS = {
    "f1 (i1⊗i3⟹i6)":        (formula_1, 6),
    "f2 ((i4⟹i6)⊗(i6⟹i2))": (formula_2, 6),
    "f6 (full, 6-var)":        (formula_6, 6),
}

# Architecture search candidates: (n_hidden_layers, hidden_width)
# Start simple, increase complexity as per paper's strategy.
ARCH_CANDIDATES = [
    (1, 4), (1, 6),
    (2, 3), (2, 4), (2, 6),
    (3, 2), (3, 4),
]


def run_reverse_engineering(
    formula_fn,
    n_vars: int,
    n_values: int = 3,
    n_trials: int = 3,
    seeds_per_arch: int = 8,
    tol_mse: float = 2e-3,
):
    table = truth_subtable(n_vars, n_values)
    targets = evaluate_formula(formula_fn, n_vars, n_values)
    print(f"  Dataset: {len(table)} samples, {n_vars} vars, {n_values}-valued")

    times = []
    for trial in range(n_trials):
        t0 = time.perf_counter()
        found = False

        for n_layers, width in ARCH_CANDIDATES:
            if found:
                break
            for seed in range(seeds_per_arch):
                torch.manual_seed(trial * 1000 + seed)
                model = make_network(n_vars, n_hidden_layers=n_layers,
                                     hidden_width=width)
                result = lm_train(model, table, targets,
                                  max_iter=400, tol_mse=tol_mse, patience=50)
                if not result["converged"]:
                    continue

                # Progressive then crisp crystallization
                # Υ_2→Υ_4→Υ_8→Υ_16 pushes weights to integers before rounding
                for m in model.net:
                    if isinstance(m, nn.Linear):
                        m.weight.data = crisp_crystallize_weights(
                            progressive_crystallize(m.weight.data)
                        )
                        m.bias.data = crisp_crystallize_bias(
                            progressive_crystallize(m.bias.data)
                        )

                delta_N = representation_error(model.flat_weights()).item()
                mse_crisp = ((model(table) - targets) ** 2).mean().item()
                if mse_crisp > tol_mse or delta_N > 0.01:
                    continue   # crystallization degraded it too much

                obs_prune(model, table, targets, mse_budget=tol_mse)

                extraction = extract_formula(model,
                                             [f"x{i+1}" for i in range(n_vars)])
                elapsed = time.perf_counter() - t0
                print(f"  Trial {trial+1}: OK  {elapsed:.1f}s  "
                      f"arch=({n_layers}×{width})  seed={seed}")
                print(f"    Formula: {extraction.formula}")
                print(f"    Representable: {extraction.representable}")
                found = True
                break

        if not found:
            elapsed = time.perf_counter() - t0
            print(f"  Trial {trial+1}: FAIL  {elapsed:.1f}s")

        times.append(time.perf_counter() - t0)

    import statistics
    mean_t = statistics.mean(times)
    std_t = statistics.stdev(times) if len(times) > 1 else 0.0
    print(f"  Summary: Mean={mean_t:.1f}s  Stdev={std_t:.1f}s")
    return mean_t, std_t


if __name__ == "__main__":
    torch.manual_seed(42)
    for name, (fn, n_vars) in FORMULAS.items():
        print(f"\n{'='*55}")
        print(f"  {name}")
        print(f"{'='*55}")
        run_reverse_engineering(fn, n_vars)
